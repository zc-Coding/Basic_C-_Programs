using System;
using System.Collections.Generic;
using System.Text;

namespace BasicMethodsApp
{
    class MathOperations
    {
        public static int Addition(int i)
        {
            int input = i;
            return input + 25;
        }

        public static int Multiply(int i)
        {
            int input = i;
            return input * 5;
        }

        public static int DivideByFive(int i)
        {
            int input = i;
            return input / 5;
        }
    }
}
